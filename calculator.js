const calculator = {
    add(a, b) {
        return a + b;
    },
    subtract(a, b) {
        return a - b;
    },
    multiply(a, b) {
        return a * b;
    },
    divide(a, b) {
        return a / b;
    }
}

console.log(calculator.add(20, 5));
console.log(calculator.subtract(20, 5));
console.log(calculator.multiply(20, 5));
console.log(calculator.divide(20, 5));